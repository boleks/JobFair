
package com.boleks.jobfair.util.beans;

public class Gradovi {
    
    private int idGrad;
    private String naziv;

    public Gradovi(int idGrad, String naziv) {
        this.idGrad = idGrad;
        this.naziv = naziv;
    }

    public int getIdGrad() {
        return idGrad;
    }

    public void setIdGrad(int idGrad) {
        this.idGrad = idGrad;
    }

    public String getNaziv() {
        return naziv;
    }

    public void setNaziv(String naziv) {
        this.naziv = naziv;
    }
    
    
    
    
    
}
